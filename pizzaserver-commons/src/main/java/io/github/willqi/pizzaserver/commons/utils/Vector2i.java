package io.github.willqi.pizzaserver.commons.utils;

public class Vector2i {

    private final int x;
    private final int y;


    public Vector2i(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }

}
